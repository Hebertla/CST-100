
public class ComputeAreaPerimeterofaCircle {

	public static void main(String[] args) {
		double radius = 5.5;
			
                //calculate perimeter and area of a circle
		double perimeter = 2 * radius * 3.14159;
		double area = radius * radius * 3.14159;
				
System.out.println("The perimeter of a circle with " +  radius + 
        " radius is  " + perimeter);


System.out.println("The area of a circle with " +  radius + " radius is  " 
        + area);
				
		
		
	}

}